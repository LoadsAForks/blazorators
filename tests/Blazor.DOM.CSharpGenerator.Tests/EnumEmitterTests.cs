// EnumEmitter tests: string-literal union -> C# enum emission.

using Blazor.DOM.CSharpGenerator.Emitters;
using Blazor.DOM.CSharpGenerator.IR;
using Xunit;

namespace Blazor.DOM.CSharpGenerator.Tests;

public sealed class EnumEmitterTests
{
    private static SymbolModel MakeEnumSymbol(string name, params string[] values)
    {
        var literals = values.Select((v, i) =>
            (TypeNode)new LiteralTypeNode("StringLiteral", $"\"{v}\"")).ToList();

        var typeNode = literals.Count == 1
            ? literals[0]
            : (TypeNode)new UnionTypeNode(literals);

        var decl = new DeclarationModel(
            Ordinal: 0, Kind: "typeAlias", Name: name,
            Modifiers: [], TypeParameters: [], Heritage: [],
            Members: [], Type: typeNode, Parameters: [], ReturnType: null,
            Documentation: new DocumentationModel("", [], false),
            Location: new LocationModel("test", new(1,1,0), new(1,20,19)),
            VariableKind: null, ConstructorObject: false,
            EventMap: new EventMapModel(false, []),
            NamespaceMembers: []);

        return new SymbolModel(
            Ordinal: 0, Name: name, SymbolFlags: 524288,
            Declarations: [decl], IsDeclarationMerged: false,
            Semantic: new SemanticModel(
                Status: "matched", WebIdlName: name, BindingKind: "definition",
                WebIdlMemberName: null, Classifications: ["enum"],
                Specifications: ["test"], Exposures: [],
                ExposedOnWindow: false, ExposedOnWorker: false, GlobalNames: [],
                Serializable: false, Transferable: false, SecureContext: false,
                ExtendedAttributes: [], Bindings: []));
    }

    [Fact]
    public void Emit_AlignSetting_ProducesValidEnum()
    {
        var symbol = MakeEnumSymbol("AlignSetting", "center", "end", "left", "right", "start");
        var source = EnumEmitter.Emit(symbol, "1.0.0", "Blazor.DOM");

        Assert.Contains("public enum AlignSetting", source);
        Assert.Contains("[JsonStringEnumMemberName(\"center\")]", source);
        Assert.Contains("[JsonStringEnumMemberName(\"end\")]", source);
        Assert.Contains("[JsonConverter(typeof(JsonStringEnumConverter<AlignSetting>))]", source);
        Assert.Contains("namespace Blazor.DOM;", source);
        Assert.DoesNotContain("object", source);
    }

    [Fact]
    public void Emit_SingleLiteral_ProducesEnum()
    {
        var symbol = MakeEnumSymbol("SingleVal", "only");
        var source = EnumEmitter.Emit(symbol, "1.0.0", "Blazor.DOM");
        Assert.Contains("public enum SingleVal", source);
        Assert.Contains("Only", source);
    }

    [Fact]
    public void Emit_SpecialCharLiteral_ProducesSafeEnumMemberName()
    {
        // Values like "audio/mpeg" should become valid C# identifiers
        var symbol = MakeEnumSymbol("MimeType", "audio/mpeg", "video/mp4");
        var source = EnumEmitter.Emit(symbol, "1.0.0", "Blazor.DOM");
        // Should not contain '/' in enum member name
        Assert.DoesNotContain("audio/mpeg", source.Split('\n')
            .Where(l => !l.TrimStart().StartsWith("[")).ToArray()
            .Aggregate((a, b) => a + b));
    }

    [Fact]
    public void Emit_IsAutoGenerated_ContainsHeader()
    {
        var symbol = MakeEnumSymbol("AlignSetting", "center");
        var source = EnumEmitter.Emit(symbol, "1.0.0", "Blazor.DOM");
        Assert.StartsWith("#nullable enable", source);
        Assert.Contains("// <auto-generated/>", source);
    }

    [Fact]
    public void Emit_OutputIsLFTerminated()
    {
        var symbol = MakeEnumSymbol("AlignSetting", "center");
        var source = EnumEmitter.Emit(symbol, "1.0.0", "Blazor.DOM");
        Assert.DoesNotContain("\r\n", source);
    }
}
