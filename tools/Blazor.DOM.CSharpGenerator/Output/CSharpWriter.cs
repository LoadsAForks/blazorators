// CSharpWriter: deterministic, indented C# source code builder.
// Produces LF-terminated output with no timestamps or checkout-dependent paths.

using System.Text;

namespace Blazor.DOM.CSharpGenerator.Output;

/// <summary>
/// Deterministic, indented C# source writer. All output is LF-terminated.
/// </summary>
public sealed class CSharpWriter
{
    private readonly StringBuilder _sb = new();
    private int _indent;
    private const int IndentSize = 4;

    public static string AutoGeneratedHeader(string generatorName, string generatorVersion)
        => $"// <auto-generated/>\n// Generator : {generatorName} v{generatorVersion}\n// Source    : data/Blazor.DOM (manifest-locked JSONL IR)\n// Do not edit this file manually.\n#nullable enable\n";

    public void AppendLine(string line = "")
    {
        if (line.Length == 0)
        {
            _sb.Append('\n');
            return;
        }
        _sb.Append(' ', _indent * IndentSize);
        _sb.Append(line);
        _sb.Append('\n');
    }

    public void Append(string text) => _sb.Append(text);

    public void OpenBrace(string? header = null)
    {
        if (header is not null)
        {
            _sb.Append(' ', _indent * IndentSize);
            _sb.Append(header);
            _sb.Append('\n');
        }
        _sb.Append(' ', _indent * IndentSize);
        _sb.Append("{\n");
        _indent++;
    }

    public void CloseBrace(string suffix = "")
    {
        _indent--;
        _sb.Append(' ', _indent * IndentSize);
        _sb.Append('}');
        _sb.Append(suffix);
        _sb.Append('\n');
    }

    public void Block(string header, Action body, string suffix = "")
    {
        AppendLine(header);
        _sb.Append(' ', _indent * IndentSize);
        _sb.Append("{\n");
        _indent++;
        body();
        _indent--;
        _sb.Append(' ', _indent * IndentSize);
        _sb.Append('}');
        _sb.Append(suffix);
        _sb.Append('\n');
    }

    /// <summary>Emits a ///-style summary doc comment.</summary>
    public void XmlDoc(string? text, bool deprecated = false)
    {
        if (!string.IsNullOrWhiteSpace(text))
        {
            // Trim and normalize the documentation text
            var lines = text.Trim()
                .Split('\n', StringSplitOptions.None);
            AppendLine("/// <summary>");
            foreach (var line in lines)
            {
                var trimmed = line.Trim();
                if (trimmed.Length == 0)
                    AppendLine("///");
                else
                    AppendLine($"/// {EscapeXmlDoc(trimmed)}");
            }
            AppendLine("/// </summary>");
        }
        if (deprecated)
            AppendLine("[Obsolete]");
    }

    private static string EscapeXmlDoc(string text)
    {
        var decoded = System.Net.WebUtility.HtmlDecode(text);
        return decoded
            .Replace("&", "&amp;", StringComparison.Ordinal)
            .Replace("<", "&lt;", StringComparison.Ordinal)
            .Replace(">", "&gt;", StringComparison.Ordinal);
    }

    public override string ToString() => _sb.ToString();
}
