// Enum emitter: projects TypeScript string-literal union typeAliases matched to
// WebIDL "enum" classification into C# enums with [JsonStringEnumMemberName] attributes.

using Blazor.DOM.CSharpGenerator.IR;
using Blazor.DOM.CSharpGenerator.Output;
using Blazor.DOM.CSharpGenerator.Projection;

namespace Blazor.DOM.CSharpGenerator.Emitters;

public static class EnumEmitter
{
    /// <summary>
    /// Emits a C# enum file for a symbol classified as a WebIDL enum.
    /// Returns the generated source or throws if the shape is unsupported.
    /// </summary>
    public static string Emit(SymbolModel symbol, string generatorVersion, string ns)
    {
        var decl = symbol.Declarations.FirstOrDefault(d => d.Kind == "typeAlias")
            ?? throw new InvalidOperationException(
                $"EnumEmitter: symbol '{symbol.Name}' has no typeAlias declaration.");

        var typeNode = decl.Type
            ?? throw new InvalidOperationException(
                $"EnumEmitter: symbol '{symbol.Name}' typeAlias has null type.");

        var members = ExtractStringLiteralMembers(symbol.Name, typeNode);
        return Emit(symbol, decl, members, generatorVersion, ns);
    }

    public static string EmitStringValues(
        SymbolModel symbol,
        IReadOnlyList<string> values,
        string generatorVersion,
        string ns)
    {
        var decl = symbol.Declarations.FirstOrDefault(d => d.Kind == "typeAlias")
            ?? throw new InvalidOperationException(
                $"EnumEmitter: symbol '{symbol.Name}' has no typeAlias declaration.");
        if (values.Count == 0)
            throw new InvalidOperationException(
                $"EnumEmitter: symbol '{symbol.Name}' has an empty finite string domain.");

        var seen = new HashSet<string>(StringComparer.Ordinal);
        var members = values.Select(value =>
        {
            var memberName = Naming.ToEnumMemberName(value);
            if (!seen.Add(memberName))
            {
                var suffix = 1;
                while (!seen.Add($"{memberName}_{suffix}"))
                    suffix++;
                memberName = $"{memberName}_{suffix}";
            }
            return (value, memberName);
        }).ToList();
        return Emit(symbol, decl, members, generatorVersion, ns);
    }

    private static string Emit(
        SymbolModel symbol,
        DeclarationModel decl,
        IReadOnlyList<(string Raw, string MemberName)> members,
        string generatorVersion,
        string ns)
    {

        var w = new CSharpWriter();
        w.AppendLine("#nullable enable");
        w.AppendLine(CSharpWriter.AutoGeneratedHeader("Blazor.DOM.CSharpGenerator", generatorVersion));
        w.AppendLine("using System.Runtime.Serialization;");
        w.AppendLine("using System.Text.Json.Serialization;");
        w.AppendLine();
        w.AppendLine($"namespace {Naming.ToGeneratedNamespace(ns, symbol.Name)};");
        w.AppendLine();

        var docText = decl.Documentation?.Text ?? "";
        var deprecated = decl.Documentation?.Deprecated ?? false;
        w.XmlDoc(docText, deprecated);

        if (symbol.Semantic.SecureContext)
            w.AppendLine("// Requires secure context (HTTPS).");

        var csName = Naming.ToCSharpSimpleTypeName(symbol.Name);
        w.AppendLine($"[JsonConverter(typeof(JsonStringEnumConverter<{csName}>))]");
        w.Block($"public enum {csName}", () =>
        {
            for (var i = 0; i < members.Count; i++)
            {
                var (raw, memberName) = members[i];
                var comma = i < members.Count - 1 ? "," : "";
                // [EnumMember] documents the intended JSON value on all TFMs.
                w.AppendLine($"[EnumMember(Value = \"{raw}\")]");
                // [JsonStringEnumMemberName] is .NET 9+ — enforces the value in System.Text.Json.
                w.AppendLine("#if NET9_0_OR_GREATER");
                w.AppendLine($"[JsonStringEnumMemberName(\"{raw}\")]");
                w.AppendLine("#endif");
                w.AppendLine($"{memberName}{comma}");
                if (i < members.Count - 1) w.AppendLine();
            }
        });

        return w.ToString();
    }

    /// <summary>
    /// Extracts (rawValue, csharpMemberName) pairs from an all-string-literal union type.
    /// Hard-errors if the type node is not an all-string-literal union.
    /// </summary>
    public static IReadOnlyList<(string Raw, string MemberName)> ExtractStringLiteralMembers(
        string symbolName, TypeNode typeNode)
    {
        List<TypeNode> literals = typeNode switch
        {
            UnionTypeNode un => [.. un.Types],
            LiteralTypeNode lit when lit.LiteralKind == "StringLiteral" => [lit],
            _ => throw new InvalidOperationException(
                $"EnumEmitter: symbol '{symbolName}' has unexpected type shape " +
                $"'{typeNode.Kind}'. Expected a string-literal union."),
        };

        var result = new List<(string, string)>(literals.Count);
        var seen = new HashSet<string>(StringComparer.Ordinal);

        foreach (var lit in literals)
        {
            if (lit is not LiteralTypeNode slit || slit.LiteralKind != "StringLiteral")
                throw new InvalidOperationException(
                    $"EnumEmitter: symbol '{symbolName}' union member is '{lit.Kind}' (not StringLiteral). " +
                    "Use AliasEmitter for mixed unions.");

            var raw = slit.Text.Trim('"');
            var memberName = Naming.ToEnumMemberName(slit.Text);
            if (!seen.Add(memberName))
                memberName += "_" + (seen.Count - 1);
            seen.Add(memberName);
            result.Add((raw, memberName));
        }

        return result;
    }
}
