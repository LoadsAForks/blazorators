using Blazor.DOM.CSharpGenerator.IR;
using Blazor.DOM.CSharpGenerator.Output;
using Blazor.DOM.CSharpGenerator.Projection;

namespace Blazor.DOM.CSharpGenerator.Emitters;

internal sealed record ProjectedUnionArm(
    NormalizedUnionArm Source,
    TypeProjection? Projection,
    string Name,
    string TransportExpression);

internal static class UnionWrapperEmitter
{
    public static string Emit(
        string name,
        string declaredName,
        string declarationSuffix,
        string ns,
        string generatorVersion,
        IReadOnlyList<ProjectedUnionArm> arms,
        string documentation,
        bool deprecated,
        string sourceDescription)
    {
        var writer = new CSharpWriter();
        writer.AppendLine("#nullable enable");
        writer.AppendLine(CSharpWriter.AutoGeneratedHeader(
            "Blazor.DOM.CSharpGenerator",
            generatorVersion));
        writer.AppendLine("using System.Diagnostics;");
        writer.AppendLine("using System.Diagnostics.CodeAnalysis;");
        writer.AppendLine("using Microsoft.JSInterop;");
        writer.AppendLine();
        writer.AppendLine($"namespace {ns};");
        writer.AppendLine();
        writer.XmlDoc(documentation, deprecated);
        writer.AppendLine($"// TypeScript union: {sourceDescription}");
        writer.AppendLine($"[DebuggerDisplay(\"{{DebuggerDisplay,nq}}\")]");
        writer.Block(
            $"public readonly struct {declaredName} : IEquatable<{declaredName}>, " +
            "IDomUnionValue" +
            declarationSuffix,
            () => EmitBody(writer, name, declaredName, arms));
        return writer.ToString();
    }

    private static void EmitBody(
        CSharpWriter writer,
        string name,
        string declaredName,
        IReadOnlyList<ProjectedUnionArm> arms)
    {
        var hasUnclassifiedReference = arms.Count(arm =>
            arm.Source.Type is ReferenceTypeNode
            && arm.Projection?.Identity.Kind == ClrTypeKind.Reference
            && arm.Source.Type.Transport?.Kind is "js-reference" or "transferable") > 1;
        writer.Block("public enum ArmKind : byte", () =>
        {
            writer.AppendLine("Uninitialized = 0,");
            for (var index = 0; index < arms.Count; index++)
            {
                var suffix = index == arms.Count - 1 && !hasUnclassifiedReference
                    ? ""
                    : ",";
                writer.AppendLine($"{arms[index].Name} = {index + 1}{suffix}");
            }
            if (hasUnclassifiedReference)
                writer.AppendLine($"UnclassifiedReference = {arms.Count + 1}");
        });
        writer.AppendLine();
        writer.AppendLine("private readonly object? _value;");
        writer.AppendLine("public ArmKind Kind { get; }");
        writer.AppendLine("public bool IsInitialized => Kind != ArmKind.Uninitialized;");
        writer.AppendLine("int IDomUnionValue.ArmIndex => (int)Kind;");
        writer.AppendLine("DomTransportDescriptor IDomUnionValue.SelectedTransport => Kind switch");
        writer.OpenBrace();
        foreach (var arm in arms)
            writer.AppendLine($"ArmKind.{arm.Name} => {arm.TransportExpression},");
        if (hasUnclassifiedReference)
        {
            writer.AppendLine(
                "ArmKind.UnclassifiedReference => DomTransportDescriptor.JsReference(" +
                "\"unclassified union reference\"),");
        }
        writer.AppendLine(
            "_ => DomTransportDescriptor.Unsupported(\"uninitialized union\", " +
            "\"A default union value has no selected arm.\"),");
        writer.CloseBrace(";");
        writer.AppendLine(
            $"private {name}(ArmKind kind, object? value) => (Kind, _value) = (kind, value);");
        writer.AppendLine();

        foreach (var arm in arms)
        {
            if (arm.Source.Special != UnionSpecialArm.None)
            {
                writer.AppendLine(
                    $"public static {declaredName} From{arm.Name}() => " +
                    $"new(ArmKind.{arm.Name}, null);");
                writer.AppendLine($"public bool Is{arm.Name} => Kind == ArmKind.{arm.Name};");
                writer.AppendLine();
                continue;
            }

            var type = arm.Projection!.RenderedType;
            writer.AppendLine(
                $"public static {declaredName} From{arm.Name}({type} value) => " +
                $"new(ArmKind.{arm.Name}, value);");
            writer.AppendLine($"public bool Is{arm.Name} => Kind == ArmKind.{arm.Name};");
            writer.AppendLine(
                $"public {type} Get{arm.Name}() => Kind == ArmKind.{arm.Name}" +
                $" ? ({type})_value! : throw new InvalidOperationException(" +
                $"$\"The union contains '{{Kind}}', not '{arm.Name}'.\");");
            writer.AppendLine(
                $"public bool TryGet{arm.Name}([MaybeNullWhen(false)] out {type} value)");
            writer.OpenBrace();
            writer.AppendLine($"if (Kind == ArmKind.{arm.Name})");
            writer.OpenBrace();
            writer.AppendLine($"value = ({type})_value!;");
            writer.AppendLine("return true;");
            writer.CloseBrace();
            writer.AppendLine("value = default;");
            writer.AppendLine("return false;");
            writer.CloseBrace();
            writer.AppendLine();
        }

        if (hasUnclassifiedReference)
        {
            writer.AppendLine(
                $"public static {declaredName} FromUnclassifiedReference(" +
                "DomUnclassifiedReference value) => new(" +
                "ArmKind.UnclassifiedReference, value ?? throw new " +
                "ArgumentNullException(nameof(value)));");
            writer.AppendLine(
                "public bool IsUnclassifiedReference => " +
                "Kind == ArmKind.UnclassifiedReference;");
            writer.AppendLine(
                "public DomUnclassifiedReference GetUnclassifiedReference() => " +
                "IsUnclassifiedReference ? (DomUnclassifiedReference)_value! : throw new " +
                "InvalidOperationException(\"The union contains a classified arm.\");");
            foreach (var candidate in arms.Where(candidate =>
                candidate.Source.Type is ReferenceTypeNode
                && candidate.Projection?.Identity.Kind == ClrTypeKind.Reference
                && candidate.Source.Type.Transport?.Kind is "js-reference" or "transferable"))
            {
                writer.AppendLine(
                    $"public {candidate.Projection!.RenderedType} " +
                    $"TakeUnclassifiedAs{candidate.Name}(" +
                    $"Func<IJSObjectReference, {candidate.Projection.RenderedType}> " +
                    "candidateFactory) => GetUnclassifiedReference().TakeAs(" +
                    "candidateFactory);");
            }
            writer.AppendLine();
        }

        writer.AppendLine(
            $"public bool Equals({declaredName} other) => Kind == other.Kind " +
            "&& EqualityComparer<object?>.Default.Equals(_value, other._value);");
        writer.AppendLine(
            $"public override bool Equals(object? obj) => obj is {declaredName} other " +
            "&& Equals(other);");
        writer.AppendLine(
            "public override int GetHashCode() => HashCode.Combine(Kind, _value);");
        writer.AppendLine(
            $"public static bool operator ==({declaredName} left, {declaredName} right) " +
            "=> left.Equals(right);");
        writer.AppendLine(
            $"public static bool operator !=({declaredName} left, {declaredName} right) " +
            "=> !left.Equals(right);");
        writer.AppendLine(
            "public override string ToString() => Kind switch");
        writer.OpenBrace();
        writer.AppendLine("ArmKind.Uninitialized => \"(uninitialized)\",");
        foreach (var arm in arms)
        {
            var text = arm.Source.Special switch
            {
                UnionSpecialArm.Null => "\"null\"",
                UnionSpecialArm.Undefined => "\"undefined\"",
                _ => "_value?.ToString() ?? \"null\"",
            };
            writer.AppendLine($"ArmKind.{arm.Name} => {text},");
        }
        if (hasUnclassifiedReference)
            writer.AppendLine("ArmKind.UnclassifiedReference => \"(unclassified reference)\",");
        writer.AppendLine("_ => \"(invalid)\",");
        writer.CloseBrace(";");
        writer.AppendLine(
            "private string DebuggerDisplay => $\"{Kind}: {ToString()}\";");
    }

    public static IReadOnlyList<ProjectedUnionArm> ProjectArms(
        NormalizedUnion normalized,
        Func<NormalizedUnionArm, TypeProjection> project)
    {
        var projected = normalized.Arms
            .Select(arm =>
            {
                var projection = arm.Special == UnionSpecialArm.None
                    ? project(arm)
                    : null;
                return new ProjectedUnionArm(
                    arm,
                    projection,
                    GetSemanticName(arm, projection),
                    GetTransportExpression(arm));
            })
            .ToList();
        var nameGroups = projected
            .GroupBy(arm => arm.Name, StringComparer.Ordinal)
            .Where(group => group.Count() > 1)
            .Select(group => group.Key)
            .ToHashSet(StringComparer.Ordinal);
        var named = projected.Select(arm =>
            nameGroups.Contains(arm.Name)
            && arm.Source.Special == UnionSpecialArm.None
            ? arm with
            {
                Name = SemanticTypeNaming.DescribeContextualProjection(
                    arm.Projection!,
                    arm.Source.Provenances[0]),
            }
            : arm).ToList();
        var collision = named
            .GroupBy(arm => arm.Name, StringComparer.Ordinal)
            .FirstOrDefault(group => group.Count() > 1);
        if (collision is not null)
        {
            var provenance = collision
                .SelectMany(arm => arm.Source.Provenances)
                .FirstOrDefault() ?? "anonymous union";
            throw new TypeProjectionException(
                $"Union arms at '{provenance}' still share the semantic name " +
                $"'{collision.Key}' after detailed type projection. Add a semantic " +
                "naming rule for this distinction; ordinal arm names are not permitted.",
                provenance);
        }
        return named;
    }

    private static string GetSemanticName(
        NormalizedUnionArm arm,
        TypeProjection? projection)
    {
        if (arm.Special != UnionSpecialArm.None)
            return GetBaseName(arm.Type, arm.Special);
        if (projection is not null
            && (arm.Type is ArrayTypeNode or TupleTypeNode or TypeLiteralTypeNode
                || arm.Type is ReferenceTypeNode reference
                && reference.Name is "Array" or "ReadonlyArray" or "Iterable"
                    or "AsyncIterable" or "PromiseLike" or "Record"))
        {
            return SemanticTypeNaming.DescribeContextualProjection(
                projection,
                arm.Provenances[0]);
        }
        return GetBaseName(arm.Type, arm.Special);
    }

    public static void ValidateRuntimeArms(
        string unionName,
        IReadOnlyList<ProjectedUnionArm> arms)
    {
        foreach (var group in arms
            .Where(arm => arm.Projection is not null)
            .GroupBy(arm => arm.Projection!.CanonicalType, StringComparer.Ordinal))
        {
            if (group.Count() < 2)
                continue;
            var items = group.ToList();
            if (items.All(item => item.Source.Type is LiteralTypeNode))
                continue;
            var transportKinds = items
                .Select(item => item.Source.Type.Transport?.Kind)
                .ToList();
            if (transportKinds.All(kind => kind is not null)
                && transportKinds.Distinct(StringComparer.Ordinal).Count()
                    == transportKinds.Count)
            {
                continue;
            }
            if (items.All(item => item.Source.Type.Transport?.Kind == "binary")
                && items.Select(item => item.Source.Type.CheckerType)
                    .Distinct(StringComparer.Ordinal)
                    .Count() == items.Count)
            {
                continue;
            }
            throw new GenericDeferralException(
                $"Union '{unionName}' has distinct TypeScript arms at " +
                $"[{string.Join(", ", items.SelectMany(item => item.Source.Provenances))}] " +
                $"that project to the same CLR type '{group.Key}' without an inbound " +
                "runtime discriminator.",
                items[1].Source.Provenances[0],
                "typed-union-arm-discriminator");
        }

    }

    private static string GetBaseName(TypeNode type, UnionSpecialArm special)
    {
        if (special == UnionSpecialArm.Null)
            return "Null";
        if (special == UnionSpecialArm.Undefined)
            return "Undefined";
        var source = type switch
        {
            ReferenceTypeNode reference => reference.Name,
            KeywordTypeNode keyword => keyword.Name.Replace("Keyword", "", StringComparison.Ordinal),
            ArrayTypeNode => "Array",
            TupleTypeNode => "Tuple",
            TypeLiteralTypeNode => "Record",
            LiteralTypeNode literal => SemanticTypeNaming.DescribeLiteral(literal.Text),
            FunctionTypeNode => "Callback",
            _ => type.Kind,
        };
        return Naming.ToCSharpSimpleTypeName(source);
    }

    private static string GetTransportExpression(NormalizedUnionArm arm)
    {
        if (arm.Special != UnionSpecialArm.None)
        {
            return $"DomTransportDescriptor.JsonValue(\"{arm.Special.ToString().ToLowerInvariant()}\", " +
                "nullable: true)";
        }
        var transport = arm.Type.Transport;
        var sourceType = Escape(transport?.SourceType ?? arm.Type.CheckerType ?? arm.Type.Kind);
        var nullable = transport?.Nullable == true ? "true" : "false";
        var streamable = transport?.Streamable == true ? "true" : "false";
        var structuredClone = transport?.StructuredClone == true ? "true" : "false";
        return transport?.Kind switch
        {
            "js-reference" =>
                $"DomTransportDescriptor.JsReference(\"{sourceType}\", nullable: {nullable}, " +
                $"streamable: {streamable}, structuredClone: {structuredClone})",
            "transferable" =>
                $"DomTransportDescriptor.Transferable(\"{sourceType}\", nullable: {nullable})",
            "binary" =>
                $"DomTransportDescriptor.Binary(\"{sourceType}\", nullable: {nullable}, " +
                $"streamable: {streamable})",
            "js-stream" =>
                $"DomTransportDescriptor.JsStream(\"{sourceType}\", nullable: {nullable})",
            "unsupported" =>
                $"DomTransportDescriptor.Unsupported(\"{sourceType}\", " +
                $"\"{Escape(transport.Reason ?? "No authoritative arm transport.")}\", " +
                $"nullable: {nullable})",
            _ => $"DomTransportDescriptor.JsonValue(\"{sourceType}\", nullable: {nullable})",
        };
    }

    private static string Escape(string value)
        => value.Replace("\\", "\\\\", StringComparison.Ordinal)
            .Replace("\"", "\\\"", StringComparison.Ordinal);
}
